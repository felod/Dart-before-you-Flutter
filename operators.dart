main() {
  var operand1 = 7;
  var operand2 = 4.5;
  
  print (operand1 + operand2); //Addition operator.
  print (operand1 - operand2); //Subtraction operator.
  print ( - operand1); //Return the negative value of this integer.
  print (operand1 * operand2); //Multiplication operator.
  print (operand1 / operand2); //Division operator.
  print (operand1 ~/ operand2); //Truncating division operator.
  print (operand1 % operand2); //Euclidean modulo operator.
}
